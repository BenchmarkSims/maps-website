// Get the flight/aircraft callsign by UHF frequency
function getCallsignByFreq(freq) {
    var i = 0;
    var callsign = "";
    var frequency = freq * 1000 >> 0;
    for (i = 0; i < radio_map.length; i++) {
        if (radio_map[i].uhf1 == frequency ||
            radio_map[i].uhf2 == frequency ||
            radio_map[i].vhf  == frequency) {
                callsign = radio_map[i].callsign;
                break;
        }
    }
    return callsign;
}
